    void cargar(){
        jTextField1.setText(Conexion.Conector.base);
        jTextField2.setText(Conexion.Conector.usuario);
        jPasswordField1.setText(Conexion.Conector.clave);
        jTextField3.setText(Conexion.Conector.ip.replace(":", ""));
        jTextField4.setText(Conexion.Conector.puerto.replace("/", ""));
    }
    
    void guardar(){
        File archivo = new File(path()+"seguridad.conexion");
        String conexion = 
                  jTextField1.getText()+"," 
                + jPasswordField1.getText()+","
                + jTextField3.getText()+":,"
                + jTextField4.getText()+"/,"
                + jTextField2.getText() + ",";
            try(PrintWriter wr = new PrintWriter(new BufferedWriter(new FileWriter(archivo)))){
                wr.write(conexion);
                dispose();
            } catch (IOException ex) {
            Logger.getLogger(datosConexion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
