    DefaultTableModel cargar(){
        DefaultTableModel model = null;
        Connection cn = Conector.conexion();
        try{
            model = (DefaultTableModel)jTable1.getModel();
            String sql = "SELECT * FROM vistademos WHERE (vehiculo LIKE ? OR serie LIKE ? OR placas LIKE ?) AND _agencia LIKE ?";
                
            String [] registros = new String [jTable1.getColumnCount()];
            int x = 0;
            String aux = null;
            model.setRowCount(0);
            PreparedStatement pst = cn.prepareStatement(sql);
                pst.setString(1, "%"+jTextField1.getText()+"%");
                pst.setString(2, "%"+jTextField1.getText()+"%");
                pst.setString(3, "%"+jTextField1.getText()+"%");
                pst.setString(4, "%"+Sesion.nombreagencia+"%");
            ResultSet rs = pst.executeQuery();
            while(rs.next()){
                    
                registros[0] = rs.getString("id");
                registros[1] = rs.getString("vehiculo");
                registros[2] = rs.getString("serie");
                registros[3] = rs.getString("placas");
                registros[4] = rs.getString("observaciones");
                registros[5] = (rs.getBoolean("libre")==true)?"En uso":"Libre";
                registros[6] = rs.getString("_agencia");
                if(Inicio.Sesion.admin){
                    registros[7] = rs.getString("creador");
                    registros[8] = rs.getString("editor");
                    registros[9] = rs.getString("actualizo");
                }
                model.addRow(registros);
                x = rs.getRow();
                aux = Integer.toString(x);
            }
            model.fireTableDataChanged();
        } catch (SQLException e) {
            new Logs.Error().error(e.getMessage());
        }
        try{ cn.close(); }catch(SQLException e){ new Logs.Error().error(e.getMessage()); }
        return model;
    }
    
