    void botonera(){
        JInternalFrame[] frame = vPantalla.getAllFrames();
        for(int i = 0;i<frame.length;i++){
            int id = i;
            final JButton btn = new JButton();
                btn.setPreferredSize(new Dimension(15,13));
                btn.setSize(new Dimension(15,13));
                btn.setMinimumSize(new Dimension(15,13));
                btn.setFocusable(false);
                btn.setOpaque(false);
                btn.setBorder(BorderFactory.createLoweredSoftBevelBorder());
            btn.addMouseListener(new MouseAdapter(){
                @Override
                public void mouseClicked(MouseEvent e) {
                    try{
                        if(!frame[id].isMaximum()){
                            frame[id].setMaximum(true);
                            frame[id].setVisible(true);
                            btn.setForeground(Color.black);                    
                        }if(frame[id].isMaximum()){
                            frame[id].setVisible(true);
                            frame[id].pack();
                            frame[id].moveToFront();
                        }
                        frame[id].setFocusable(true);
                        if(e.getClickCount() == 2){
                            frame[id].setIcon(true);
                        }
                    } catch (PropertyVetoException ex) {
                        Logs.emergente.mostrar(ex.getMessage(),"Mensaje");
                    }
                }
            });
            frame[id].addKeyListener(new KeyAdapter(){
                @Override
                public void keyPressed(KeyEvent ke) {
                    if (ke.getKeyCode() == KeyEvent.VK_ESCAPE){
                        try{
                            frame[id].dispose();
                        }catch(Exception e){
                            //Nada que cerrar
                        }
                    }
                }
            });
            
            frame[id].addInternalFrameListener(new InternalFrameAdapter(){
                String titulo = String.format("%-20s",frame[id].getTitle()).replace(" "," ").substring(0,20);
                @Override                
                public void internalFrameOpened(InternalFrameEvent e) {
                    try{
                        Dimension tamaño = frame[id].getSize();
                        frame[id].setPreferredSize(tamaño);
                        frame[id].setMinimumSize(tamaño);
                        frame[id].setBackground(new Color(238,238,238));
                        frame[id].setFocusable(true);
                        
                        Dimension p = Principal.Escritorio.vPantalla.getSize();
                        Dimension v = frame[id].getSize();
                        
                        frame[id].setLocation((p.width - v.width)/2, (p.height - v.height)/2);
                        
                        btn.setText(titulo);
                        btn.setToolTipText(frame[id].getTitle());
                        btn.setIcon(frame[id].getFrameIcon());
                        btn.setForeground(Color.black);                    
                        vBarra.add(btn);
                    }catch(Exception ex){
                        Logs.emergente.mostrar(getTitle()+" "+ex.getMessage(),"Mensaje");
                    }
                }
                @Override
                public void internalFrameClosed(InternalFrameEvent e) {
                    vBarra.remove(btn);
                    vBarra.repaint();
                    frame[id] = null;
                }
                @Override
                public void internalFrameIconified(InternalFrameEvent e) {
                    btn.setForeground(Color.gray);                    
                    frame[id].setVisible(false);
                }
                
                @Override
                public void internalFrameDeactivated(InternalFrameEvent e){
                    btn.setForeground(Color.gray);                    
                }
                @Override
                public void internalFrameActivated(InternalFrameEvent e){
                    btn.setForeground(Color.black);                    
                }
            });
        }
    }

