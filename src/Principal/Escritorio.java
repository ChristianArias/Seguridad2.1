    void fondo(){
        String imagen = "";
        switch(Sesion.nombreagencia){
            case "Galerias":
                imagen = Conexion.Conector.path()+"lib" + 
                        Conexion.Conector.s+"Fondo"+
                        Conexion.Conector.s+"Galerias.jpg";
                break;
            case "Milenio Motors":
                imagen = Conexion.Conector.path()+"lib"+Conexion.Conector.s+"Fondo"+Conexion.Conector.s+"Milenio.jpg";
                break;
            default:
                imagen =    Conexion.Conector.path()+"lib"+
                            Conexion.Conector.s+"Fondo"+
                            Conexion.Conector.s+"default.jpg";
                break;
        }
        vPantalla.setBorder(new other.ImagenInicial(imagen));
    }
