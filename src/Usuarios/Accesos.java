    DefaultTableModel cargar(int id){
        Connection cn = Conector.conexion();
        DefaultTableModel model =(DefaultTableModel)jTable1.getModel();
        try{
            String sql = "SELECT id,date_format(hora,'%r') as hora,fecha,ultimaip FROM accesos "
                    + "WHERE id = ? ORDER BY fecha DESC";
            String [] registros = new String [jTable1.getColumnCount()];
            int x = 0;
            String aux = null;
            model.setRowCount(0);
            PreparedStatement pst = cn.prepareStatement(sql);
                pst.setInt(1, id);
            ResultSet rs = pst.executeQuery();
            while(rs.next()){                
                registros[0] = rs.getString("hora");
                registros[1] = rs.getString("fecha");
                registros[2] = rs.getString("ultimaip");
                model.addRow(registros);
                x = rs.getRow();
                aux = Integer.toString(x);
            }
            model.fireTableDataChanged();
        }catch(SQLException e){
            new Logs.Error().error(e.getMessage());
        }
        try{ cn.close(); }catch(SQLException e){ new Logs.Error().error(e.getMessage()); }
        return model;
    }

