    public void setCellRenderer(JTable jTable1) {
        Enumeration<TableColumn> en = jTable1.getColumnModel().getColumns();
        while (en.hasMoreElements()) {
            TableColumn tc = en.nextElement();
            tc.setCellRenderer(new other.CellRenderer());
        }
    }
    
    DefaultTableModel cargar(){
        Connection cn = Conector.conexion();
        DefaultTableModel model =(DefaultTableModel)jTable1.getModel();
        try{
            String sql = "SELECT * FROM vistausuarios WHERE (nombre LIKE ? OR usuario LIKE ?) "
                    + "AND id NOT LIKE ? AND usuario NOT LIKE ? ORDER BY activo DESC";
            
            String [] registros = new String [jTable1.getColumnCount()];
            int x = 0;
            String aux = null;
            model.setRowCount(0);
            PreparedStatement pst = cn.prepareStatement(sql);
                pst.setString(1, "%"+jTextField1.getText()+"%");
                pst.setString(2, "%"+jTextField1.getText()+"%");
                pst.setInt(3, Sesion.id);
                pst.setString(4, "root");
            ResultSet rs = pst.executeQuery();
            while(rs.next()){
                
                registros[0] = rs.getString("id");
                registros[1] = rs.getString("nombre");
                registros[2] = rs.getString("usuario");
                registros[3] = rs.getString("comentarios");
                registros[4] = (rs.getBoolean("activo")==true)?ok:fals;
                registros[5] = rs.getString("_agencia");
                
                model.addRow(registros);
                x = rs.getRow();
                aux = Integer.toString(x);
            }
            model.fireTableDataChanged();
        }catch(SQLException e){
            new Logs.Error().error(e.getMessage());
        }
        try{ cn.close(); }catch(SQLException e){ new Logs.Error().error(e.getMessage()); }
        return model;
    }
    
    void cambiarStatus(boolean bool,int id){
        Connection cn = Conector.conexion();
        try{
            String sql = "UPDATE seg_usuario SET activo = ? WHERE id = ?";
            PreparedStatement pst = cn.prepareStatement(sql);
                pst.setBoolean(1, bool);
                pst.setInt(2, id);
            int n = pst.executeUpdate();
            if(n>0){
                jTable1.setModel(cargar());
            }
        }catch(SQLException e){
            new Logs.Error().error(e.getMessage());
        }
        try{ cn.close(); }catch(SQLException e){ new Logs.Error().error(e.getMessage()); }
    }
    
