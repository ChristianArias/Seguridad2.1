    void seleccionar(JPanel p,boolean bol){
        for(int x = 0; x<p.getComponentCount();x++){
            if(p.getComponent(x)instanceof JCheckBox){
                JCheckBox check = (JCheckBox) p.getComponent(x);
                check.setSelected(bol);
            }
        }
    }
    
    void cargar(int sel){
        try{
            String sql = "SELECT * FROM seg_usuario WHERE id = "+sel;
            ResultSet rs = new Mysql.ResultSet().rs(sql).executeQuery();
            while(rs.next()){
                id = sel;
                jTextField1.setText(rs.getString("nombre")+" ["+rs.getString("usuario")+"]");
                setTitle("Permisos a "+rs.getString("usuario"));
            }
        }catch(SQLException e){
            new Logs.Error().error(e.getMessage());
        }
    }
    
    void mode(){
        col = val = "";
        for(int c = 0; c<jPanel1.getComponentCount();c++){
            if(jPanel1.getComponent(c)instanceof JCheckBox){
                JCheckBox check = (JCheckBox) jPanel1.getComponent(c);
                val += "m" + check.getName() + " = " + check.isSelected() + " , ";
                col += "m" + check.getName()+",";
            }
        }
    }
    
    void guardar(){
        mode();
        Connection cn = Conector.conexion();
        try{
            String sql = "INSERT INTO seg_usuariopermisos(usuario,m0,m1,m2,m3,m4,m5,m6,m7,m8,m9,m10,m11,m12)"
                    +   "VALUES("+id+","+val.substring(0, val.length()-2)+") "
                    +   "ON DUPLICATE KEY UPDATE "+val.substring(0, val.length()-2);
            PreparedStatement  pst = cn.prepareStatement(sql);
            int n = pst.executeUpdate();
            if(n>0){
                Logs.emergente.mostrar("Permisos actualizados", "Mensaje");
                dispose();
            }
        }catch(SQLException e){
            new Logs.Error().error(e.getMessage());
        }
        try{ cn.close(); }catch(SQLException e){ new Logs.Error().error(e.getMessage()); }
    }
    
    void cargarModulos(int sel){
        Connection cn = Conector.conexion();
        try{
            String sql = "SELECT * FROM seg_usuariopermisos WHERE usuario = ?";
            PreparedStatement  pst = cn.prepareStatement(sql);
                pst.setInt(1, sel);
            ResultSet rs = pst.executeQuery();
            while(rs.next()){
                jCheckBox2.setSelected(rs.getBoolean("m0"));
                jCheckBox3.setSelected(rs.getBoolean("m1"));
                jCheckBox4.setSelected(rs.getBoolean("m2"));
                jCheckBox5.setSelected(rs.getBoolean("m3"));
                jCheckBox6.setSelected(rs.getBoolean("m4"));
                jCheckBox7.setSelected(rs.getBoolean("m5"));
                jCheckBox8.setSelected(rs.getBoolean("m6"));
                jCheckBox9.setSelected(rs.getBoolean("m7"));
                jCheckBox10.setSelected(rs.getBoolean("m8"));
                jCheckBox11.setSelected(rs.getBoolean("m9"));
                jCheckBox12.setSelected(rs.getBoolean("m10"));
                jCheckBox13.setSelected(rs.getBoolean("m11"));
                jCheckBox14.setSelected(rs.getBoolean("m12"));
            }
        }catch(SQLException e){
            new Logs.Error().error(e.getMessage());
        }
        try{ cn.close(); }catch(SQLException e){ new Logs.Error().error(e.getMessage()); }
    }

