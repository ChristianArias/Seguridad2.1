    public class valor {
        private final int id ;
        private final String nombre;
        private final String dato;
  
        public valor(int id,String nombre,String dato) {
            this.id         =   id;
            this.nombre     =   nombre;
            this.dato       =   dato;
        }
 
        public int getID(){
            return id ;
        }
 
        @Override
        public String toString() {
            return nombre ;
        }
        
        public String getDato(){
            return dato;
        }
    }
    
