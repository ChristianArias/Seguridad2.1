    boolean verificar(){
        boolean res = false;
        Connection cn = Conector.conexion();
        try{
            String sql = "SELECT * FROM agencias WHERE nombre = ?";
            PreparedStatement pst = cn.prepareStatement(sql);
                pst.setString(1,jTextField1.getText());
            ResultSet rs = pst.executeQuery();
            while(rs.next()){
                res = true;
                Logs.emergente.mostrar(rs.getString(2)+" ya existe.", "Mensaje");
            }
        }catch(SQLException e){
            new Logs.Error().error(e.getMessage());
        }
        return res;
    }
    
    void cargar(int sel){
        Connection cn = Conector.conexion();
        try{
            String sql = "SELECT * FROM agencias WHERE id = ?";
            PreparedStatement pst = cn.prepareStatement(sql);
                pst.setInt(1,sel);
            ResultSet rs = pst.executeQuery();
            while(rs.next()){
                nombre = rs.getString(2);
                setTitle("Actualizar "+nombre);
                jTextField1.setText(rs.getString(2));
                jTextArea1.setText(rs.getString(3));
            }
        }catch(SQLException e){
            new Logs.Error().error(e.getMessage());
        }
    }

    void actualizar(){
        Connection cn = Conector.conexion();
        try{
            String sql = "UPDATE agencias SET nombre = ?,comentarios = ? WHERE id = ?";
            PreparedStatement pst = cn.prepareStatement(sql);
                pst.setString(1, jTextField1.getText());
                pst.setString(2, jTextArea1.getText());
                pst.setInt(3, id);
            int n = pst.executeUpdate();
            if( n > 0 ){
                Logs.emergente.mostrar("Se actualizo", "Mensaje");
                dispose();
            }
        }catch(SQLException e){
            new Logs.Error().error(e.getMessage());
        }
    }
    
    void agregar(){
        Connection cn = Conector.conexion();
        try{
            String sql = "INSERT INTO agencias (nombre,comentarios) VALUES (?,?)";
            PreparedStatement pst = cn.prepareStatement(sql);
                pst.setString(1, jTextField1.getText());
                pst.setString(2, jTextArea1.getText());
            int n = pst.executeUpdate();
            if( n > 0 ){
                Logs.emergente.mostrar("Se agrego", "Mensaje");
                dispose();
            }
        }catch(SQLException e){
            new Logs.Error().error(e.getMessage());
        }
    }
    
